    <h3>Why Vote Swapping?</h3>
  
    <p>Often times people don't end up voting for the party they want to see
    elected. Instead they <a
    href="http://en.wikipedia.org/wiki/Strategic_voting" target="_blank">vote
    strategically</a> for the party in their riding with
    the best chance of defeating the party they dislike the most.</p>
    
    <p>This is where vote swapping can help. Two voters in different ridings
    agree to vote for each other's party of preference. This site helps you
    connect with people in other ridings who are willing to vote for the party
    you support in exchange for your vote for the party they support.</p>
  </div>

  <div class="col col-2">
    <h3>Can I Trust You?</h3>
    
    <p>This is about making every vote count, not party politics. Vote Swap
    Canada is not affiliated with any party.</p>
    
    <p>Also, the beauty of Vote Swap Canada is that you don't have to trust
    us. You need to trust the person you match up with. It's up to you to
    chat with them and evaluate whether you trust them to swap votes with you.</p>
    
    <p>Further, Vote Swap Canada's code is open source and can be
    <a href="https://github.com/bradt/vote-swap-canada">fully inspected at GitHub</a>.
